def map_old_tag_to_new(old_tag):
    """將舊標籤映射到新標籤"""
    if old_tag in TAG_MAPPING:
        return TAG_MAPPING[old_tag]
    else:
        # 如果沒有找到映射，保留原標籤但標記為需要檢查
        print(f"Warning: No mapping found for tag: {old_tag}")
        return [old_tag]

def standardize_tags(old_tags):
    """標準化標籤列表"""
    new_tags = []
    for old_tag in old_tags:
        mapped_tags = map_old_tag_to_new(old_tag)
        new_tags.extend(mapped_tags)

    # 去重並排序
    return sorted(list(set(new_tags)))

